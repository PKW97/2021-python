
a, b = input("실수 두개 입력 >> ").split()

num1 = float(a)
num2 = float(b)

print("{} > {} 결과: {}".format(num1, num2, num1 > num2))
print("{} >= {} 결과: {}".format(num1, num2, num1 >= num2))
print("{} < {} 결과: {}".format(num1, num2, num1 < num2))
print("{} <= {} 결과: {}".format(num1, num2, num1 <= num2))
print("{} == {} 결과: {}".format(num1, num2, num1 == num2))
print("{} != {} 결과: {}".format(num1, num2, num1 != num2))
